class TestStatus(Enum):
    """Status of a test execution."""
    PASSED = "passed"
    FAILED = "failed"
    ERROR = "error"
    SKIPPED = "skipped"


@dataclass
class TestCase:
    """
    A single test case for NL2SQL evaluation.
    
    Attributes:
        test_id: Unique identifier for the test
        category: Category of the test (e.g., aggregation, filtering, joins)
        complexity: Complexity level (e.g., simple, medium, complex)
        question: Natural language question to convert to SQL
        expected_sql: Expected SQL query
        tags: List of tags for categorization and filtering
    """
    test_id: str
    category: str
    complexity: str
    question: str
    expected_sql: str
    tags: List[str] = field(default_factory=list)
    expected_result_contains: List[str] = field(default_factory=list)
    expected_row_count: Optional[int] = None
    description: str = ""
    skip: bool = False
    skip_reason: str = ""
    
    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> 'TestCase':
        """Create a TestCase from a dictionary."""
        # Handle tags as comma-separated string or list
        tags = data.get('tags', [])
        if isinstance(tags, str):
            tags = [t.strip() for t in tags.split(',') if t.strip()]
        
        # Handle expected_result_contains
        expected_contains = data.get('expected_result_contains', [])
        if isinstance(expected_contains, str):
            expected_contains = [s.strip() for s in expected_contains.split(',') if s.strip()]
        
        return cls(
            test_id=str(data.get('test_id', data.get('id', 'unknown'))),
            category=data.get('category', 'general'),
            complexity=data.get('complexity', 'medium'),
            question=data.get('question', data.get('query', data.get('natural_language', ''))),
            expected_sql=data.get('expected_sql', data.get('sql', data.get('ground_truth', ''))),
            tags=tags,
            expected_result_contains=expected_contains,
            expected_row_count=data.get('expected_row_count'),
            description=data.get('description', ''),
            skip=data.get('skip', False),
            skip_reason=data.get('skip_reason', '')
        )


@dataclass
class TestResult:
    """
    Result of executing a single test case.
    
    Attributes:
        test_id: Test case identifier
        question: The natural language question (for reference)
        generated_sql: SQL generated by the system
        expected_sql: Expected SQL (if provided)
        sql_executed: Whether the SQL executed successfully
        sql_error: Error message if execution failed
        execution_time_ms: Time to generate and execute SQL
        category: Category from the test case
    """
    test_id: str
    question: str
    generated_sql: str
    expected_sql: str
    sql_executed: bool
    sql_error: Optional[str]
    execution_time_ms: float
    category: str
    complexity: str = "medium"
    status: TestStatus = TestStatus.FAILED
    result_match: bool = False
    row_count: int = 0
    column_names: List[str] = field(default_factory=list)
    generation_time_ms: float = 0.0
    notes: str = ""
    tags: List[str] = field(default_factory=list)
    
    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary for serialization."""
        d = asdict(self)
        d['status'] = self.status.value
        return d


@dataclass
class TestReport:
    """
    Aggregate report of all test results.
    """
    timestamp: str
    total_tests: int
    passed: int
    failed: int
    errors: int
    skipped: int
    pass_rate: float
    results: List[TestResult]
    errors_by_type: Dict[str, List[str]] = field(default_factory=dict)
    results_by_tag: Dict[str, Dict[str, int]] = field(default_factory=dict)
    results_by_category: Dict[str, Dict[str, int]] = field(default_factory=dict)
    results_by_complexity: Dict[str, Dict[str, int]] = field(default_factory=dict)
    execution_stats: Dict[str, float] = field(default_factory=dict)
    
    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary for serialization."""
        return {
            'timestamp': self.timestamp,
            'total_tests': self.total_tests,
            'passed': self.passed,
            'failed': self.failed,
            'errors': self.errors,
            'skipped': self.skipped,
            'pass_rate': self.pass_rate,
            'results': [r.to_dict() for r in self.results],
            'errors_by_type': self.errors_by_type,
            'results_by_tag': self.results_by_tag,
            'results_by_category': self.results_by_category,
            'results_by_complexity': self.results_by_complexity,
            'execution_stats': self.execution_stats
        }


