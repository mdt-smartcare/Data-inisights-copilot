"""
SQL Test Runner - Automated NL2SQL accuracy evaluation against golden datasets.

This script runs natural language to SQL tests against a database and generates
reports on accuracy, execution success, and regression detection.

Run with:
    python -m app.scripts.run_sql_tests --input tests.csv --output results.json --report report.md --user-id test_user
    python -m app.scripts.run_sql_tests --filter "temporal_*" --verbose
    python -m app.scripts.run_sql_tests --input tests.json --db-url "duckdb://data/test.db"

Features:
    - Loads test cases from CSV or JSON files
    - Generates SQL using the SQLService
    - Executes and compares results
    - Tracks execution time and errors
    - Generates detailed reports by category and complexity
    - Privacy-safe logging (no PII in logs)

IMPORTANT: This script does NOT log actual data results to avoid PII exposure.
Only metadata (row counts, column names, execution status) is logged.
"""
