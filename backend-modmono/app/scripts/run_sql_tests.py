def load_test_cases_csv(file_path: Path) -> List[TestCase]:
    """Load test cases from a CSV file."""
    test_cases = []
    with open(file_path, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            try:
                test_case = TestCase.from_dict(row)
                test_cases.append(test_case)
            except Exception as e:
                logger.warning(f"Failed to parse row: {e}")
    logger.info(f"Loaded {len(test_cases)} test cases from CSV: {file_path}")
    return test_cases


def load_test_cases_json(file_path: Path) -> List[TestCase]:
    """Load test cases from a JSON file."""
    with open(file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    if isinstance(data, list):
        raw_cases = data
    elif isinstance(data, dict):
        for key in ['test_cases', 'tests', 'examples', 'queries']:
            if key in data and isinstance(data[key], list):
                raw_cases = data[key]
                break
        else:
            raw_cases = [data]
    else:
        raw_cases = []
    
    test_cases = []
    for i, raw in enumerate(raw_cases):
        try:
            if 'test_id' not in raw and 'id' not in raw:
                raw['test_id'] = f"test_{i:04d}"
            test_case = TestCase.from_dict(raw)
            test_cases.append(test_case)
        except Exception as e:
            logger.warning(f"Failed to parse test case {i}: {e}")
    
    logger.info(f"Loaded {len(test_cases)} test cases from JSON: {file_path}")
    return test_cases


def load_test_cases(file_path: Path) -> List[TestCase]:
    """Load test cases from a file (auto-detect format)."""
    suffix = file_path.suffix.lower()
    if suffix == '.csv':
        return load_test_cases_csv(file_path)
    elif suffix == '.json':
        return load_test_cases_json(file_path)
    else:
        try:
            return load_test_cases_json(file_path)
        except json.JSONDecodeError:
            return load_test_cases_csv(file_path)


def filter_test_cases(test_cases: List[TestCase], pattern: str) -> List[TestCase]:
    """Filter test cases by pattern."""
    import fnmatch
    
    filtered = []
    
    if pattern.startswith("tag:"):
        tag_filter = pattern[4:].lower()
        for tc in test_cases:
            if any(tag_filter in t.lower() for t in tc.tags):
                filtered.append(tc)
    elif pattern.startswith("category:"):
        category_filter = pattern[9:].lower()
        for tc in test_cases:
            if category_filter in tc.category.lower():
                filtered.append(tc)
    elif pattern.startswith("complexity:"):
        complexity_filter = pattern[11:].lower()
        for tc in test_cases:
            if complexity_filter in tc.complexity.lower():
                filtered.append(tc)
    else:
        for tc in test_cases:
            if fnmatch.fnmatch(tc.test_id.lower(), pattern.lower()):
                filtered.append(tc)
    
    logger.info(f"Filter '{pattern}': {len(filtered)}/{len(test_cases)} tests selected")
    return filtered


