# eval/sql_eval/sql_evaluator.py
"""
SQL Evaluator — Enhanced with schema-compliance metrics.

Evaluates Text-to-SQL queries on two dimensions:
1. Execution Equivalence: Does the generated SQL produce the same results?
2. Schema Compliance: Does the SQL use correct tables, joins, filters?

Schema compliance metrics catch "right answer, wrong query" problems
where a bad join or missing filter coincidentally produces correct output.
"""
import re
import pandas as pd
import logging
from typing import Dict, Any, List, Optional

logger = logging.getLogger("eval.sql")


class SQLEvaluator:
    """
    Evaluates Text-to-SQL logic by executing the generated SQL against a test DB
    and comparing the resulting DataFrame to the execution result of the Ground Truth SQL.
    
    Enhanced with schema-compliance metrics:
    - Table accuracy: Are the correct tables referenced?
    - Join correctness: Are FK joins used instead of arbitrary conditions?
    - Filter compliance: Are default filters (is_active, is_deleted) present?
    - Aggregation match: Are the expected aggregation functions used?
    """
    
    def __init__(self, db_engine=None):
        """
        Initialize with a SQLAlchemy engine pointing to the test database.
        Engine is optional — schema compliance checks work without a live DB.
        """
        self.engine = db_engine

    # =========================================================================
    # Core Evaluation
    # =========================================================================
    
    def evaluate_query(self, query: str, generated_sql: str, ground_truth_sql: str,
                       expected_tables: List[str] = None,
                       expected_joins: List[str] = None,
                       expected_filters: List[str] = None,
                       expected_aggregations: List[str] = None) -> Dict[str, Any]:
        """
        Execute both SQL statements and compare their DataFrames + schema compliance.
        
        Args:
            query: Natural language question
            generated_sql: SQL generated by the pipeline
            ground_truth_sql: Known-correct SQL
            expected_tables: Tables that should appear in the query
            expected_joins: Expected join conditions (e.g., "patient_tracker.site_id = site.id")
            expected_filters: Columns that should appear in WHERE clause
            expected_aggregations: Expected aggregation functions (COUNT, AVG, etc.)
        
        Returns:
            Dict with execution equivalence + schema compliance metrics
        """
        result = {
            "query": query,
            "generated_sql": generated_sql,
            "ground_truth_sql": ground_truth_sql,
            "is_equivalent": False,
            "execution_error": None,
            # Schema compliance metrics
            "schema_compliance": {},
            "overall_score": 0.0,
        }

        # 1. Schema compliance checks (work without DB)
        compliance = self._check_schema_compliance(
            generated_sql,
            expected_tables=expected_tables,
            expected_joins=expected_joins,
            expected_filters=expected_filters,
            expected_aggregations=expected_aggregations,
        )
        result["schema_compliance"] = compliance
        
        # 2. Execution equivalence (requires DB)
        if self.engine and ground_truth_sql:
            try:
                exec_result = self._check_execution_equivalence(generated_sql, ground_truth_sql)
                result["is_equivalent"] = exec_result["is_equivalent"]
                result["execution_error"] = exec_result.get("error")
            except Exception as e:
                result["execution_error"] = f"Execution error: {e}"
        elif not self.engine:
            result["execution_error"] = "No database engine — execution equivalence skipped"
        elif not ground_truth_sql:
            result["execution_error"] = "Missing ground truth SQL"
        
        # 3. Compute overall score (weighted average)
        result["overall_score"] = self._compute_overall_score(result)
        
        return result
    
    # =========================================================================
    # Execution Equivalence
    # =========================================================================
    
    def _check_execution_equivalence(self, generated_sql: str, ground_truth_sql: str) -> Dict[str, Any]:
        """Execute both queries and compare DataFrames."""
        try:
            df_gt = pd.read_sql_query(ground_truth_sql, self.engine)
            df_gen = pd.read_sql_query(generated_sql, self.engine)
            
            try:
                pd.testing.assert_frame_equal(
                    df_gt.sort_index(axis=1).sort_values(by=df_gt.columns.tolist()).reset_index(drop=True),
                    df_gen.sort_index(axis=1).sort_values(by=df_gen.columns.tolist()).reset_index(drop=True),
                    check_dtype=False,
                    check_exact=False,
                    check_names=False
                )
                return {"is_equivalent": True}
            except AssertionError as e:
                return {"is_equivalent": False, "error": f"DataFrames do not match: {str(e)[:200]}"}
                
        except Exception as e:
            return {"is_equivalent": False, "error": f"SQL execution failed: {str(e)[:200]}"}
    
    # =========================================================================
    # Schema Compliance Checks
    # =========================================================================
    
    def _check_schema_compliance(
        self,
        generated_sql: str,
        expected_tables: List[str] = None,
        expected_joins: List[str] = None,
        expected_filters: List[str] = None,
        expected_aggregations: List[str] = None,
    ) -> Dict[str, Any]:
        """
        Check if generated SQL follows expected schema patterns.
        
        Returns per-dimension scores (0.0-1.0) and detailed issues.
        """
        compliance = {
            "table_accuracy": 1.0,
            "join_correctness": 1.0,
            "filter_compliance": 1.0,
            "aggregation_match": 1.0,
            "issues": [],
        }
        
        sql_lower = generated_sql.lower()
        
        # --- Table Accuracy ---
        if expected_tables:
            tables_found = self._extract_tables(sql_lower)
            missing = [t for t in expected_tables if t not in tables_found]
            extra = [t for t in tables_found if t not in expected_tables and t not in ('dual', 'generate_series')]
            
            if missing:
                compliance["issues"].append(f"Missing tables: {missing}")
            if extra:
                compliance["issues"].append(f"Extra tables: {extra}")
            
            if expected_tables:
                correct = len(expected_tables) - len(missing)
                compliance["table_accuracy"] = correct / len(expected_tables) if expected_tables else 1.0
        
        # --- Join Correctness ---
        if expected_joins:
            join_score = self._check_joins(sql_lower, expected_joins, compliance["issues"])
            compliance["join_correctness"] = join_score
        
        # --- Filter Compliance ---
        if expected_filters:
            filter_score = self._check_filters(sql_lower, expected_filters, compliance["issues"])
            compliance["filter_compliance"] = filter_score
        
        # --- Aggregation Match ---
        if expected_aggregations:
            agg_score = self._check_aggregations(sql_lower, expected_aggregations, compliance["issues"])
            compliance["aggregation_match"] = agg_score
        
        return compliance
    
    def _extract_tables(self, sql_lower: str) -> List[str]:
        """Extract table names from SQL query."""
        # Match FROM and JOIN clauses
        pattern = r'(?:from|join)\s+([a-z_][a-z0-9_]*)'
        return list(set(re.findall(pattern, sql_lower)))
    
    def _check_joins(self, sql_lower: str, expected_joins: List[str], issues: list) -> float:
        """Check if expected join conditions are present."""
        if not expected_joins:
            return 1.0
        
        found = 0
        for join in expected_joins:
            # Normalize: "table_a.col = table_b.col" → check both directions
            parts = join.lower().replace(" ", "")
            if "=" in parts:
                left, right = parts.split("=", 1)
                # Check if either direction exists in the SQL
                if left in sql_lower.replace(" ", "") and right in sql_lower.replace(" ", ""):
                    found += 1
                elif right in sql_lower.replace(" ", "") and left in sql_lower.replace(" ", ""):
                    found += 1
                else:
                    issues.append(f"Missing join: {join}")
        
        return found / len(expected_joins)
    
    def _check_filters(self, sql_lower: str, expected_filters: List[str], issues: list) -> float:
        """Check if expected filter columns appear in WHERE clause."""
        if not expected_filters:
            return 1.0
        
        # Find WHERE clause content
        where_start = sql_lower.find('where')
        if where_start < 0:
            issues.append(f"No WHERE clause found, expected filters: {expected_filters}")
            return 0.0
        
        where_clause = sql_lower[where_start:]
        
        found = 0
        for filter_col in expected_filters:
            if filter_col.lower() in where_clause:
                found += 1
            else:
                issues.append(f"Missing filter: {filter_col}")
        
        return found / len(expected_filters)
    
    def _check_aggregations(self, sql_lower: str, expected_aggs: List[str], issues: list) -> float:
        """Check if expected aggregation functions are used."""
        if not expected_aggs:
            return 1.0
        
        found = 0
        for agg in expected_aggs:
            agg_lower = agg.lower()
            if agg_lower in sql_lower:
                found += 1
            else:
                issues.append(f"Missing aggregation: {agg}")
        
        return found / len(expected_aggs)
    
    # =========================================================================
    # Scoring
    # =========================================================================
    
    def _compute_overall_score(self, result: Dict) -> float:
        """
        Compute weighted overall score (0.0 - 1.0).
        
        Weights:
        - Execution equivalence: 40% (if available)
        - Table accuracy: 20%
        - Join correctness: 15%
        - Filter compliance: 15%
        - Aggregation match: 10%
        """
        compliance = result.get("schema_compliance", {})
        
        # Schema compliance score (60% of total when DB available, 100% otherwise)
        schema_score = (
            compliance.get("table_accuracy", 1.0) * 0.20 +
            compliance.get("join_correctness", 1.0) * 0.15 +
            compliance.get("filter_compliance", 1.0) * 0.15 +
            compliance.get("aggregation_match", 1.0) * 0.10
        )
        
        if self.engine and result.get("execution_error") is None:
            # Full evaluation with DB
            exec_score = 1.0 if result.get("is_equivalent") else 0.0
            return exec_score * 0.40 + schema_score / 0.60 * 0.60
        else:
            # Schema-only evaluation (normalize to 0-1)
            return schema_score / 0.60
    
    # =========================================================================
    # Batch Evaluation
    # =========================================================================
    
    def evaluate_batch(self, entries: List[Dict]) -> Dict[str, Any]:
        """
        Evaluate a batch of golden dataset entries.
        
        Args:
            entries: List of golden dataset entry dicts
            
        Returns:
            Aggregate metrics dict
        """
        results = []
        
        for entry in entries:
            generated_sql = entry.get("generated_sql", "")
            if not generated_sql:
                logger.warning(f"Skipping {entry.get('id', '?')} — no generated SQL")
                continue
            
            result = self.evaluate_query(
                query=entry.get("query", ""),
                generated_sql=generated_sql,
                ground_truth_sql=entry.get("ground_truth_sql", ""),
                expected_tables=entry.get("expected_tables"),
                expected_joins=entry.get("expected_joins"),
                expected_filters=entry.get("expected_filters"),
                expected_aggregations=entry.get("expected_aggregations"),
            )
            result["id"] = entry.get("id", "unknown")
            result["category"] = entry.get("category", "unknown")
            result["difficulty"] = entry.get("difficulty", "unknown")
            results.append(result)
        
        return self._aggregate_results(results)
    
    def _aggregate_results(self, results: List[Dict]) -> Dict[str, Any]:
        """Aggregate individual results into summary metrics."""
        if not results:
            return {"error": "No results to aggregate"}
        
        total = len(results)
        equivalent_count = sum(1 for r in results if r.get("is_equivalent"))
        avg_score = sum(r.get("overall_score", 0) for r in results) / total
        
        # Per-dimension averages
        avg_table = sum(r["schema_compliance"].get("table_accuracy", 0) for r in results) / total
        avg_join = sum(r["schema_compliance"].get("join_correctness", 0) for r in results) / total
        avg_filter = sum(r["schema_compliance"].get("filter_compliance", 0) for r in results) / total
        avg_agg = sum(r["schema_compliance"].get("aggregation_match", 0) for r in results) / total
        
        # By difficulty
        difficulty_scores = {}
        for diff in ("easy", "medium", "hard"):
            diff_results = [r for r in results if r.get("difficulty") == diff]
            if diff_results:
                difficulty_scores[diff] = {
                    "count": len(diff_results),
                    "avg_score": sum(r["overall_score"] for r in diff_results) / len(diff_results),
                    "equivalent_rate": sum(1 for r in diff_results if r.get("is_equivalent")) / len(diff_results),
                }
        
        # By category
        category_scores = {}
        categories = set(r.get("category", "unknown") for r in results)
        for cat in categories:
            cat_results = [r for r in results if r.get("category") == cat]
            if cat_results:
                category_scores[cat] = {
                    "count": len(cat_results),
                    "avg_score": sum(r["overall_score"] for r in cat_results) / len(cat_results),
                }
        
        return {
            "total_queries": total,
            "execution_equivalence_rate": equivalent_count / total if total else 0,
            "avg_overall_score": round(avg_score, 4),
            "schema_compliance": {
                "avg_table_accuracy": round(avg_table, 4),
                "avg_join_correctness": round(avg_join, 4),
                "avg_filter_compliance": round(avg_filter, 4),
                "avg_aggregation_match": round(avg_agg, 4),
            },
            "by_difficulty": difficulty_scores,
            "by_category": category_scores,
            "individual_results": results,
        }
